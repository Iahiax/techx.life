<?php

namespace App\Services;

/**
 * خدمة حساب العمولة (1%)
 * 
 * تقوم هذه الخدمة بحساب العمولة المستحقة للمنصة بناءً على المبلغ المحول.
 * القاعدة:
 * - 1% من قيمة المبلغ
 * - بحد أدنى 1 ريال وأقصى 10 ريالات للمبالغ التي تقل عن أو تساوي 1,000,000 ريال
 * - إذا زاد المبلغ عن 1,000,000 ريال، تُحتسب الـ 1% كاملة بدون حد أقصى
 */
class FeeCalculator
{
    /**
     * حساب العمولة
     *
     * @param float $amount المبلغ المراد حساب العمولة عليه
     * @return float قيمة العمولة مقربة إلى أقرب رقمين عشريين
     */
    public function calculate(float $amount): float
    {
        // حساب 1% من المبلغ
        $fee = $amount * 0.01;

        // تطبيق الشرط: إذا كان المبلغ <= 1,000,000 فالعمولة بين 1 و 10 ريال
        if ($amount <= 1000000) {
            $fee = max(1, min(10, $fee));
        }
        // إذا كان المبلغ > 1,000,000 تبقى العمولة كما هي (1% بدون حد)

        return round($fee, 2);
    }
}
